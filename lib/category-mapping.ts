/**
 * Get product material info (model & category) from matcode
 * Returns both model description and category
 * Supports partial matches for barcodes that contain the material code
 */
export const getMaterialInfoFromMatcode = (matcode: string): MaterialInfo => {
  if (!matcode) return { model: "Unknown", category: "Others" };

  const code = String(matcode).toUpperCase().trim();
  
  // 1. EXACT MATCH - Highest priority
  if (Object.prototype.hasOwnProperty.call(MATCODE_MATERIAL_MAP, code)) {
    return MATCODE_MATERIAL_MAP[code];
  }
  
  // 2. PARTIAL MATCH - Try to match beginning of barcode with known material codes
  // Look for known prefixes in the material code map
  const possibleMatches = Object.keys(MATCODE_MATERIAL_MAP).filter(key => 
    code.startsWith(key) || key.startsWith(code.substring(0, Math.min(code.length, key.length)))
  );
  
  // Find the best match (longest matching prefix)
  if (possibleMatches.length > 0) {
    // Sort by length descending to get the best match
    possibleMatches.sort((a, b) => b.length - a.length);
    
    // Check if any of the possible matches is likely a material code
    // Material codes are typically 9-12 characters
    const bestMatch = possibleMatches.find(match => {
      const matchLength = match.length;
      // If the match is at least 8 characters, it's likely a material code
      return matchLength >= 8 && code.startsWith(match);
    });
    
    if (bestMatch) {
      return MATCODE_MATERIAL_MAP[bestMatch];
    }
  }
  
  // 3. FALLBACK TO CATEGORY DETECTION
  const category = getCategoryFromBinCode(code);
  
  // For category detection, also try to extract material code
  // Common Haier material code patterns
  const materialPatterns = [
    /^([A-Z]\d{2}[A-Z]{2}\d[M|E]\d[A-Z])/, // Pattern like B30FZ4M6K
    /^([A-Z]{2}\d{2}[A-Z]\d[M|E]\d{2})/,   // Pattern like BS08X2EA6
    /^([A-Z]{2}\d{5}[A-Z])/,               // Pattern like TD0043841
    /^([A-Z]\d{3}[A-Z]{2}\d[M|E]\d{2})/,   // Pattern like B40791E6H
  ];
  
  for (const pattern of materialPatterns) {
    const match = code.match(pattern);
    if (match) {
      const extractedCode = match[1];
      // Try to find this extracted code in our mapping
      if (Object.prototype.hasOwnProperty.call(MATCODE_MATERIAL_MAP, extractedCode)) {
        return MATCODE_MATERIAL_MAP[extractedCode];
      }
    }
  }

  // 4. FINAL FALLBACK
  return { model: code, category };
};

